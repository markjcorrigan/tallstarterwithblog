            <h2>Overview</h2>
            <p>Some of the core IT laws are discussed below.</p>
        </section>
        <section>
            <h2>Brook's Law</h2>
            <p>Brooks' law is a claim about software project management according to which "adding manpower to a late software project makes it later".</p>
        </section>
        <section>
            <h2>Parkinson's Law</h2>
            <p>Is the adage that "work expands so as to fill the time available for its completion".</p>
        </section>
        <section>
            <h2>Miller's Law</h2>
            <p>George Miller, a professor of psychology, showed that, at any one time, we humans are capable of concentrating on only approximately seven chunks (units of information).</p>
        </section>
        <section>
            <h2>Linus's Law</h2>
            <p>Linus's Law as described by Raymond is a claim about software development, named in honor of Linus Torvalds and formulated by Raymond in his essay and book 'The Cathedral and the Bazaar (1999)'.</p>
        </section>
        <section>
            <h2>Conway's Law</h2>
            <p>Conway's law is an adage named after computer programmer Melvin Conway, who introduced the idea in 1967.</p>
        </section>
        <section>
            <h2>Goodheart's Law</h2>
            <p>When a measure becomes the target, it ceases to be a good measure.</p>
        </section>
