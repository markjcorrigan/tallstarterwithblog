                                softare regularly each sprint that meets the user requirement (<a
                                    href="/productincrement" target=_blank"><i><u>the art of slicing sprint delivery /
                                            product increment production</u></i></a>). Requirements are accepted by the
                                team as user stories into the current sprint. Therefore the gap in Scrum typically, is a
                                2 week sprint!<br><br><b>Here are some important questions you may want to consider, if
                                    your aim is to operate with agility at Capability Maturity Level 2+:<br>
                                    <ul align="left">
                                        <li>Can the team mind the gap releasing user stories as working software at the
                                            end of each sprint? <br>I.e. <a href="/home/agile?slide=12" target="_blank"><u>From
                                                    the Agile Manifesto's two key principles which state:<br><br>

                                                    <ul>
                                                        <li>
                                                </u></a> “Our highest priority is to satisfy the customer through early
                                            and continuous delivery of valuable software”
                                        </li>
                                        &amp
                                        <li>"Deliver working software frequently, from a couple of weeks to a couple of
                                            months, with a preference on the shorter timescale"
                                        </li>
                                    </ul>
                                    <br><i>Note: As the Scrum method can be used for all types of projects (not just
                                        software projects) <a href="/home/snowbird" target="_blank"><u><br>Per the last
                                                10 minutes of the discussion here</u></a> it is suggested that the word
                                        "software" can easily be swopped out for the word "value" <br>I.e. (in the Agile
                                        Manifesto's four value propositions:<br> We are uncovering better ways of
                                        developing software by doing it and helping others do it.<br><a
                                            href="/home/agile?slide=12" target="_blank"><u>Through this work we have
                                                come to value:...</u></a>)</i></li>
                                    <li>Essentially, are the team a <a href="/images/doneandundone.jpg" target="_blank">done
                                            or undone</a> Scrum team?
                                    </li>
                                    <li>If an undone Scrum team, are they dealing with more and more <a
                                            href="/images/undonescrum.jpg" target="_blank">undone work</a> piling up
