                            <p><small>Remember that a project, stage, phase or iteration should all qualify to fall
                                    under this definition of what is a project? It's a temporary endeavor undertaken to
                                    create a unique product, service or result. A project is temporary in that it has a
                                    defined beginning and end in time, and therefore defined scope and resources. ELSE,
